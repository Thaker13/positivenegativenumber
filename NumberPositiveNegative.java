package numberpositivenegative;

public class NumberPositiveNegative {

    public static void main(String[] args) {

        int value = 3;
        if (value < 0) {

            System.out.println(value + "is Negative number");

        } else if (value > 0) {

            System.out.println(value + "is positive number");

        } else {

            System.out.println(value + "is Zero number");
        }
    }

}
